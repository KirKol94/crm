  TEMPORARY_REDIRECT: 307, // Временное перенаправление. Запрос должен быть направлен на другой URL (сохраняя метод запроса).
  PERMANENT_REDIRECT: 308, // Постоянное перенаправление. Ресурс был перенесен на новый URL и запросы должны использовать новый URL.

  // Ошибки клиента (400-499)
