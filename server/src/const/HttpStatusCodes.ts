  // Успешные коды (200-299)
  OK: 200, // ОК. Запрос выполнен успешно.
  CREATED: 201, // Создано. Запрос успешно создал новый ресурс.
  ACCEPTED: 202, // Принято. Запрос принят, но его выполнение может занять некоторое время.
  NON_AUTHORITATIVE_INFORMATION: 203, // Неполная информация. Информация в ответе может быть неполной, поскольку сервер возвращает только ту часть ресурса, которая может быть отображена на текущий момент.
  NO_CONTENT: 204, // Нет содержимого. Запрос выполнен успешно, но ответ не содержит содержимого (например, при обновлении или удалении).
